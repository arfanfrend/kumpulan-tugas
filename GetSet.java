public class GetSet {
    private int uas;
    private int uts;
    private String nama;
    private String grade;


    public int getUas(){
        return uas;

    }


    public int getUts(){
        return uts;

    }


    public String getNama(){
        return nama;

    }


    public String getGrade(){
        return grade;

    }

    public void setUas(int uas){
        this.uas = uas;
    }

    public void setUts(int uts){
        this.uts = uts;
    }

    public void setNama(String nama){
        this.nama = nama;
    }

    public void setGrade(String grade){
        this.grade = grade;
    }
}
